        {
            throw new NotImplementedException();
        }

        public override MembershipUser GetUser(string username, bool userIsOnline)
        {
            if (UserManager.User != null)
            {
                return new MembershipUser(nameof(MyMembershipProvider), username, "", "", "", null, true, false, DateTime.Now, DateTime.Now, DateTime.Now, DateTime.Now, DateTime.Now);
            }

            return null;
        }

